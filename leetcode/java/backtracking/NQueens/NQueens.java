package leetcode.java.backtracking.NQueens;

import java.util.ArrayList;
import java.util.List;

/* 51. N-Queens
https://leetcode.com/problems/n-queens/description/

Загадка с n ферзями — это задача о том, как разместить n ферзей на шахматной доске размера n x n так, чтобы никакие два
ферзя не атаковали друг друга.

Учитывая целое число n, найдите все различные решения головоломки с n ферзями. Вы можете вернуть ответ в любом порядке.

Каждое решение содержит отдельную конфигурацию доски для размещения n ферзей, где «Q» и «.». оба указывают на ферзя и
пустое место соответственно.

Input: n = 4
Output: [[".Q..","...Q","Q...","..Q."],["..Q.","Q...","...Q",".Q.."]]
Пояснение: существует два различных решения головоломки с четырьмя ферзями, как показано выше.

Input: n = 1
Output: [["Q"]]
*/

public class NQueens {
    public static void main(String[] args) {
        System.out.println(solveNQueens(4)); // [[.Q.., ...Q, Q..., ..Q.], [..Q., Q..., ...Q, .Q..]]
        System.out.println(solveNQueens(1)); // [[Q]]
    }

    /**
     * Решает задачу о расстановке n ферзей и возвращает все возможные решения.
     *
     * @param n размер доски (n×n) и количество ферзей
     * @return список всех валидных расстановок, где каждая расстановка — список строк
     * <p>
     * Time: O(n!) — факториальная сложность.
     * На первой строке ≤ n позиций, на второй ≤ n-2 (исключая вертикаль и диагонали),
     * и т.д. Реальное количество проверок меньше из-за отсечения невалидных ветвей.
     * <p>
     * Space: O(n^2) — память под доску (char[n][n]) + O(n) глубина рекурсии.
     * С учётом вывода: O(n! * n^2) для хранения всех решений.
     */
    private static List<List<String>> solveNQueens(int n) {
        List<List<String>> result = new ArrayList<>();

        // Инициализируем шахматную доску: '.' — пустая клетка, 'Q' — ферзь
        char[][] board = new char[n][n];
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                board[i][j] = '.';
            }
        }

        backtrack(result, board, 0, n);
        return result;
    }

    /**
     * Рекурсивная функция обратного отслеживания для поиска всех валидных расстановок.
     * <p>
     * Принцип работы:
     * - Размещаем ферзей построчно: на строке `row` пытаемся поставить ферзя в каждую колонку
     * - Перед установкой проверяем, не атакует ли текущую позицию уже поставленный ферзь
     * - Если позиция валидна — ставим ферзя, рекурсивно переходим к следующей строке
     * - После возврата из рекурсии выполняем «откат»: убираем ферзя, чтобы попробовать другую колонку
     * - Когда все строки обработаны (row == n) — сохраняем копию доски в результат
     *
     * @param result список для накопления итоговых расстановок
     * @param board  текущее состояние доски (char[][] для эффективности)
     * @param row    текущая строка, в которую пытаемся поставить ферзя
     * @param n      размер доски
     */
    private static void backtrack(List<List<String>> result, char[][] board, int row, int n) {
        // Базовый случай: все ферзи успешно расставлены
        if (row == n) {
            result.add(boardToString(board));
            return;
        }

        // Перебираем все колонки в текущей строке
        for (int col = 0; col < n; col++) {
            if (isValid(board, row, col, n)) {
                board[row][col] = 'Q';           // Ставим ферзя
                backtrack(result, board, row + 1, n); // Рекурсия к следующей строке
                board[row][col] = '.';           // Откат: убираем ферзя
            }
        }
    }

    /**
     * Проверяет, можно ли безопасно поставить ферзя на позицию (row, col).
     * <p>
     * Поскольку ферзи ставятся построчно сверху вниз, достаточно проверить:
     * 1. Вертикаль вверх (столбец `col` в строках 0..row-1)
     * 2. Диагональ влево-вверх (↖)
     * 3. Диагональ вправо-вверх (↗)
     * <p>
     * Горизонтали и нижние диагонали проверять не нужно — там ещё нет ферзей.
     *
     * @param board текущее состояние доски
     * @param row   строка для проверки
     * @param col   колонка для проверки
     * @param n     размер доски
     * @return true если позиция безопасна, false иначе
     * <p>
     * Time: O(n) — в худшем случае проверяем ~3n клеток
     */
    private static boolean isValid(char[][] board, int row, int col, int n) {
        // Проверка вертикали вверх
        for (int i = 0; i < row; i++) {
            if (board[i][col] == 'Q') {
                return false;
            }
        }

        // Проверка диагонали влево-вверх (↖)
        for (int i = row - 1, j = col - 1; i >= 0 && j >= 0; i--, j--) {
            if (board[i][j] == 'Q') {
                return false;
            }
        }

        // Проверка диагонали вправо-вверх (↗)
        for (int i = row - 1, j = col + 1; i >= 0 && j < n; i--, j++) {
            if (board[i][j] == 'Q') {
                return false;
            }
        }

        return true;
    }

    /**
     * Конвертирует доску из char[][] в формат списка строк для вывода.
     *
     * @param board двумерный массив символов ('Q' или '.')
     * @return список строк, где каждая строка — представление ряда доски
     * <p>
     * Time: O(n^2) — проходим по всем клеткам доски
     */
    private static List<String> boardToString(char[][] board) {
        List<String> representation = new ArrayList<>();
        for (char[] row : board) {
            representation.add(new String(row));
        }
        return representation;
    }
}
