package leetcode.java.backtracking.TargetSum;

import java.util.HashMap;
import java.util.Map;

/* 494. Target Sum
https://leetcode.com/problems/target-sum/description/

Вам дан целочисленный массив nums и целочисленная цель.
Вы хотите построить выражение из чисел, добавив один из символов «+» и «-» перед каждым целым числом в числах, а затем
объединить все целые числа.
Например, если nums = [2, 1], вы можете добавить «+» перед 2 и «-» перед 1 и объединить их, чтобы построить выражение
«+2-1».
Верните количество различных выражений, которые вы можете создать и которые достигают целевого значения.

Input: nums = [1,1,1,1,1], target = 3
Output: 5
Пояснение: Есть 5 способов назначить символы, чтобы сумма чисел соответствовала целевому значению 3.
-1 + 1 + 1 + 1 + 1 = 3
+1 - 1 + 1 + 1 + 1 = 3
+1 + 1 - 1 + 1 + 1 = 3
+1 + 1 + 1 - 1 + 1 = 3
+1 + 1 + 1 + 1 - 1 = 3

Input: nums = [1], target = 1
Output: 1
*/

public class TargetSum {
    public static void main(String[] args) {
        int[] nums = {1, 1, 1, 1, 1};
        System.out.println(findTargetSumWays(nums, 3)); // Вывод: 5
    }

    /**
     * Возвращает количество способов расставить знаки '+' и '-' перед числами в массиве
     * так, чтобы их сумма равнялась целевому значению target.
     * <p>
     * Алгоритм: рекурсивный перебор с мемоизацией (динамическое программирование сверху-вниз).
     * Для каждого элемента пробуем два варианта: добавить его с '+' или с '-'.
     * <p>
     * Time: O(n * S), где n — длина массива, S — диапазон возможных сумм.
     * В худшем случае сумма может меняться от -sum(nums) до +sum(nums),
     * поэтому количество уникальных состояний: O(n * 2*sum).
     * Space: O(n * S) для хэш-таблицы мемоизации + O(n) для стека рекурсии.
     *
     * @param nums   массив неотрицательных целых чисел
     * @param target целевая сумма
     * @return количество способов получить target
     */
    private static int findTargetSumWays(int[] nums, int target) {
        Map<String, Integer> memo = new HashMap<>();
        return dp(nums, target, 0, 0, memo);
    }

    /**
     * Рекурсивная вспомогательная функция с мемоизацией для подсчёта количества способов.
     * <p>
     * На каждом шаге решаем: прибавить текущее число или вычесть его, затем рекурсивно
     * обрабатываем оставшуюся часть массива. Результаты кэшируются по ключу "index_sum".
     * <p>
     * Time: O(n * S) — каждое уникальное состояние (index, sum) вычисляется один раз.
     * Space: O(n * S) для кэша + O(n) для глубины рекурсии.
     *
     * @param nums   исходный массив чисел
     * @param target целевая сумма
     * @param index  текущий индекс в массиве
     * @param sum    текущая накопленная сумма
     * @param memo   хэш-таблица для кэширования результатов подзадач
     * @return количество способов достичь target из текущего состояния
     */
    private static int dp(int[] nums, int target, int index, int sum, Map<String, Integer> memo) {
        // Формируем уникальный ключ для состояния (index, sum)
        String key = index + "_" + sum;

        // Проверяем кэш: если результат уже вычислен, возвращаем его
        if (memo.containsKey(key)) {
            return memo.get(key);
        }

        // Базовый случай: обработали все элементы
        if (index == nums.length) {
            // Если текущая сумма равна target — нашли один валидный способ
            return (sum == target) ? 1 : 0;
        }

        // Рекурсивно считаем количество способов:
        // 1. Прибавляем текущее число к сумме
        int positive = dp(nums, target, index + 1, sum + nums[index], memo);
        // 2. Вычитаем текущее число из суммы
        int negative = dp(nums, target, index + 1, sum - nums[index], memo);

        // Суммируем оба варианта
        int total = positive + negative;

        // Сохраняем результат в кэш перед возвратом
        memo.put(key, total);

        return total;
    }
}
